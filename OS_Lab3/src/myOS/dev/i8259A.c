#include "io.h"

void init8259A(void){
	//你需要填写它
	outb(0x21, 0xFF);
	outb(0xA1, 0xFF);
	outb(0x20, 0x11);
	outb(0x21, 0x20);
	outb(0x21, 0x04);
	outb(0x21, 0x03);
	outb(0xA0, 0x11);
	outb(0xA1, 0x28);
	outb(0xA1, 0x02);
	outb(0xA1, 0x01);
}
//– 屏蔽所有中断源：0xFF==》0x21和0xA1
//– 主片初始化：ICW1：0x11 ==》0x20
//ICW2：起始向量号0x20 ==》0x21
//ICW3：从片接入引脚位 0x04 ==》0x21
//ICW4：中断结束方式 AutoEOI 0x3 ==》0x21
//– 从片初始化： ICW1：0x11 ==》0xA0
//ICW2：起始向量号0x28==》0xA1
//ICW3：接入主片的编号0x02==》0xA1
//ICW4： 中断结束方式 0x01==》0xA1
